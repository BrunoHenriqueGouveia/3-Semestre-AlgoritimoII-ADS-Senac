package Aula6Ex2;

public class Fornecedor {

	private String nome, fone;

	public Fornecedor(String nome, String fone) {

		this.nome = nome;
		this.fone = fone;
	}

	public Fornecedor() {

	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getFone() {
		return fone;
	}

	public void setFone(String fone) {
		this.fone = fone;
	}
	

}
